#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

int	check_printable(char c)
{
	return (c >= 32 && c != 127);
}

void	ft_putstr_non_printable(char *str)
{
	int				index;
	unsigned char	current;

	index = 0;
	while (str[index] != '\0')
	{
		current = str[index];
		if (check_printable(current))
			ft_putchar(current);
		else
		{
			ft_putchar('\\');
			ft_putchar("0123456789abcdef"[current / 16]);
			ft_putchar("0123456789abcdef"[current % 16]);
		}
		index++;
	}
}
